/* Table 2: DT_PLAYER_PROFILE */
CREATE TABLE NEON_DATA.DT_PLAYER_PROFILE
(
    PLAYER_UUID UUID        NOT NULL,
    PLAYER_NAME VARCHAR(16) NOT NULL,
    JOIN_AT     DATETIME    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ROLE_ID     BIGINT
);

/* P.Keys */
ALTER TABLE NEON_DATA.DT_PLAYER_PROFILE
    ADD CONSTRAINT PK_DT_PLAYER_PROFILE PRIMARY KEY (PLAYER_UUID);

/* UQ.Keys */
ALTER TABLE NEON_DATA.DT_PLAYER_PROFILE
    ADD CONSTRAINT UQ_DT_PLAYER_PROFILE_PLAYER_NAME UNIQUE (PLAYER_NAME);

/* F.Keys -> DT_ROLE.ROLE_ID */
ALTER TABLE NEON_DATA.DT_PLAYER_PROFILE
    ADD CONSTRAINT FK_DT_PLAYER_PROFILE_DT_ROLE_ROLE_ID FOREIGN KEY (ROLE_ID)
        REFERENCES DT_ROLE (ROLE_ID)
        ON DELETE SET NULL;


