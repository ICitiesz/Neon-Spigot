/* Table 3: DT_ACCES_PERMISSION */
CREATE TABLE NEON_DATA.DT_ACCESS_PERMISSION
(
    PERMISSION_ID   BIGINT IDENTITY,
    PERMISSION_NAME VARCHAR(64) NOT NULL,
    PERMISSION_CODE VARCHAR(64) NOT NULL, /* E.g.: permissionCode => access_n_serverFeature */
    PERMISSION_DESC LONGVARCHAR
);

/* P.Keys */
ALTER TABLE NEON_DATA.DT_ACCESS_PERMISSION
    ADD CONSTRAINT PK_DT_ACCESS_PERMISSION PRIMARY KEY (PERMISSION_ID);

/* UQ.Keys */
ALTER TABLE NEON_DATA.DT_ACCESS_PERMISSION
    ADD CONSTRAINT UQ_DT_ACCESS_PERMISSION_PERMISSION_NAME UNIQUE (PERMISSION_NAME);

ALTER TABLE NEON_DATA.DT_ACCESS_PERMISSION
    ADD CONSTRAINT UQ_DT_ACCESS_PERMISSION_PERMISSION_CODE UNIQUE (PERMISSION_CODE);

/* Checks */
ALTER TABLE NEON_DATA.DT_ACCESS_PERMISSION
    ADD CONSTRAINT DT_ACCESS_PERMISSION_ENSURE_LOWERCASE CHECK(PERMISSION_CODE = UPPER(PERMISSION_CODE));

-- ##############################################################################################

/* Table 4: DT_ROLE_ACCESS */
CREATE TABLE NEON_DATA.DT_ROLE_ACCESS
(
    ROLE_ACCESS_ID  BIGINT IDENTITY,
    ROLE_ID         BIGINT      NOT NULL,
    PERMISSION_ID   BIGINT      NOT NULL,
    ACCESS_TYPE     VARCHAR(7)  NOT NULL
);

/* P.Keys */
ALTER TABLE NEON_DATA.DT_ROLE_ACCESS
    ADD CONSTRAINT PK_ROLE_ACCESS PRIMARY KEY (ROLE_ACCESS_ID);

/* F.Keys -> DT_ROLE.ROLE_ID, DT_ACCESS_PERMISSION.PERMISSION_ID */
ALTER TABLE NEON_DATA.DT_ROLE_ACCESS
    ADD CONSTRAINT FK_DT_ROLE_ACCESS__DT_ROLE_ROLE_ID FOREIGN KEY (ROLE_ID)
        REFERENCES NEON_DATA.DT_ROLE (ROLE_ID)
        ON DELETE CASCADE;

ALTER TABLE NEON_DATA.DT_ROLE_ACCESS
    ADD CONSTRAINT FK_DT_ROLE_ACCESS_PERMISSION__DT_ACCESS_PERMISSION_PERMISSION_ID FOREIGN KEY (PERMISSION_ID)
        REFERENCES NEON_DATA.DT_ACCESS_PERMISSION (PERMISSION_ID)
        ON DELETE CASCADE;

/* UQ.Keys */
ALTER TABLE NEON_DATA.DT_ROLE_ACCESS
    ADD CONSTRAINT UQ_DT_ROLE_ACCESS_BATCH_1 UNIQUE (ROLE_ID, PERMISSION_ID);

/* Checks */
ALTER TABLE NEON_DATA.DT_ROLE_ACCESS
    ADD CONSTRAINT ENSURE_UPPER_CASE CHECK (ACCESS_TYPE = UPPER(ACCESS_TYPE));

