/* Table 1: DT_ROLE */
CREATE TABLE NEON_DATA.DT_ROLE
(
    ROLE_ID   BIGINT IDENTITY,
    ROLE_DISPLAY_NAME VARCHAR(64) NOT NULL,
    ROLE_CODE VARCHAR(64) NOT NULL, /* E.g.: ROLE_CODE => role_admin */
    ASSIGNED_PLAYER_COUNT BIGINT NOT NULL DEFAULT 0
);

/* P.Keys */
ALTER TABLE NEON_DATA.DT_ROLE
    ADD CONSTRAINT PK_DT_ROLE PRIMARY KEY (ROLE_ID);

/* UQ.Keys */
ALTER TABLE NEON_DATA.DT_ROLE
    ADD CONSTRAINT UQ_DT_ROLE_ROLE_CODE UNIQUE (ROLE_CODE);

/* Checks */
ALTER TABLE NEON_DATA.DT_ROLE
    ADD CONSTRAINT DT_ROLE_ENSURE_UPPER CHECK(ROLE_CODE = UPPER(ROLE_CODE));

